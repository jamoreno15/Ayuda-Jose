    private void btnSaveEducationActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveEducationActionPerformed
    {//GEN-HEADEREND:event_btnSaveEducationActionPerformed
        Expense expense = new Expense();
        FrameManager frameManager = new FrameManager();
        expense.setExpense(lbExpenseEducation.getText());
        expense.setAmount(txtAmountEducation.getText());
        expense.setEstablishment(txtEstablishmentEducation.getText());
        expense.setDate(dateEducation.getDate().toString());

        if (chbxCashEducation.getState() == true) {
            expense.setPaymentMethod(chbxCashEducation.getLabel());
        }
        if (chbxDebitCardEducation.getState() == true) {
            expense.setPaymentMethod(chbxDebitCardEducation.getLabel());
        }
        if (chbxCreditCardEducation.getState() == true) {
            expense.setPaymentMethod(chbxCreditCardEducation.getLabel());
        }

        frameManager.saveExpense(expense);
       


    }//GEN-LAST:event_btnSaveEducationActionPerformed


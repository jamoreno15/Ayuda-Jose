    private void txtAmountSalaryKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAmountSalaryKeyTyped
        char limitChar = evt.getKeyChar();
        if (!Character.isDigit(limitChar) && limitChar != '.') {
            evt.consume();
        }
    }//GEN-LAST:event_txtAmountSalaryKeyTyped


    private void btnSaveClothesActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveClothesActionPerformed
    {//GEN-HEADEREND:event_btnSaveClothesActionPerformed
        Expense expense = new Expense();
        FrameManager frameManager = new FrameManager();
        expense.setExpense(lbExpenseClothes.getText());
        expense.setAmount(txtAmountClothes.getText());
        expense.setEstablishment(txtEstablishmentClothes.getText());
        expense.setDate(dtcDateClothes.getDate().toString());

        if (chbxCashClothes.getState() == true) {
            expense.setPaymentMethod(chbxCashClothes.getLabel());
        }
        if (chbxDebitCardClothes.getState() == true) {
            expense.setPaymentMethod(chbxDebitCardClothes.getLabel());
        }
        if (chbxCreditCardClothes.getState() == true) {
            expense.setPaymentMethod(chbxCreditCardClothes.getLabel());
        }
        frameManager.saveExpense(expense);
        

    }//GEN-LAST:event_btnSaveClothesActionPerformed


    public FrmExpenseOtherCategory() {
        initComponents();
        this.setLocationRelativeTo(null);

        File archivo = null;
        FileReader fileReader = null;
        BufferedReader bufferReader = null;
        ArrayList<OtherCategoryExpense> newCategory = new ArrayList<>();
        Gson gson = new Gson();
        String line;
        String newCategoryJson = "";

        try {
            archivo = new File(directory + "\\OtherCategoryExpense.json");
            fileReader = new FileReader(archivo);
            bufferReader = new BufferedReader(fileReader);

            while ((line = bufferReader.readLine()) != null) {
                System.out.println(line);

                newCategoryJson = newCategoryJson + line;

            }
            java.lang.reflect.Type newCategoryType = new TypeToken<ArrayList<OtherCategoryExpense>>() {
            }.getType();
            newCategory = gson.fromJson(newCategoryJson, newCategoryType);
            for (OtherCategoryExpense categories : newCategory) {
                newCategoryComboBox.addElement(categories.getOtherCategory());
            }
            CmbBoxOtherCategory.setModel(newCategoryComboBox);
        } catch (Exception e) {
        } finally {
            try {
                if (null != fileReader) {
                    fileReader.close();
                }
            } catch (IOException e2) {
            }

        }
    }


    private void btnSaveOtherCategoryActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveOtherCategoryActionPerformed
    {//GEN-HEADEREND:event_btnSaveOtherCategoryActionPerformed
        Expense expense = new Expense();
        FrameManager frameManager = new FrameManager();
        expense.setExpense(lbExpenseOtherCategory.getText());
        expense.setAmount(txtAmountOtherCategory.getText());
        expense.setEstablishment(CmbBoxOtherCategory.toString());
        expense.setDate(dtcDateOtherCategory.getDate().toString());

        if (chbxCash.getState() == true) {
            expense.setPaymentMethod(chbxCash.getLabel());
        }
        if (chbxDebitCard.getState() == true) {
            expense.setPaymentMethod(chbxDebitCard.getLabel());
        }
        if (chbxCreditCard.getState() == true) {
            expense.setPaymentMethod(chbxCreditCard.getLabel());
        }

        frameManager.saveExpense(expense);
        JOptionPane.showMessageDialog(this, "Save Data");


    }//GEN-LAST:event_btnSaveOtherCategoryActionPerformed


